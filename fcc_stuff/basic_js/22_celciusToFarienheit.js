/*
Challenge: Convert Celcius temp to fahrenheit.

Notes:
1. Take Celicus number
2. Times by 9
3. Devide by 5
4. Add 32
*/

function convertToF(celsius) {
  var fahrenheit;
  // Only change code below this line
  var fahrenheit = celsius * (9 / 5) + 32;

  // Only change code above this line
  return fahrenheit;
}

